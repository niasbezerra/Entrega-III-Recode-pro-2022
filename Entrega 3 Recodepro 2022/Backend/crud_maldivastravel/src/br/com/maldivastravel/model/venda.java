package br.com.maldivastravel.model;

public class venda {
	private int id_venda;
	private int id_cliente;
	private int id_destino_disponivel;
	public int getId_venda() {
		return id_venda;
	}
	public void setId_venda(int id_venda) {
		this.id_venda = id_venda;
	}
	public int getId_cliente() {
		return id_cliente;
	}
	public void setId_cliente(int id_cliente) {
		this.id_cliente = id_cliente;
	}
	public int getId_destino_disponivel() {
		return id_destino_disponivel;
	}
	public void setId_destino_disponivel(int id_destino_disponivel) {
		this.id_destino_disponivel = id_destino_disponivel;
	}
		
	
}